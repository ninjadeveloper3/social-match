class OndeviceML extends StatefulWidget {
  @override
  _OndeviceMLState createState() => _OndeviceMLState();
}

class _OndeviceMLState extends State<OndeviceML> {
  final TextEditingController _textController = TextEditingController();
  final EntityExtractor entityExtractor = EntityExtractor(
    language: EntityExtractorLanguage.english,
  );
  List<EntityAnnotation> annotations = [];
  var matchedEvent = '';
  @override
  void initState() {
    super.initState();
  }

  Future<void> _extractEntities() async {
    final text = _textController.text;
    final List<String> words = text.split(' ');

    List<String> remainingWords = [...words];

    if (text.isNotEmpty) {
      annotations = await entityExtractor.annotateText(text);
      setState(() {});
    }
    remainingWords.removeWhere((word) => stopWords.contains(word));

    if (remainingWords.isNotEmpty) {
      for (final annotation in annotations) {
        remainingWords.removeWhere((word) => annotation.text.contains(word));
      }
      matchedEvent = remainingWords.join(' ');
      print("dklfdjkfn:  $matchedEvent");
    } else {
      matchedEvent = '';
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Extract Event, place and Time'),
      ),
      body: Column(
        children: [
          TextField(
            controller: _textController,
          ),
          ElevatedButton(
            onPressed: _extractEntities,
            child: Text('Extract Entities'),
          ),
          SizedBox(height: 16),
          Text('Extracted Entities:'),
          SizedBox(height: 8),
          ListTile(
            title: Text(matchedEvent),
            subtitle: Text("Event Happening"),
          ),
          Expanded(
            child: ListView.builder(
              itemCount: annotations.length,
              itemBuilder: (context, index) {
                final annotation = annotations[index];
                final entities = annotation.entities
                    .map((entity) => '${entity.type}: ${entity.rawValue}')
                    .join(', ');

                return ListTile(
                  title: Text(annotation.text),
                  subtitle: Text(entities),
                );
              },
            ),
          ),
        ],
      ),
    );
  }

